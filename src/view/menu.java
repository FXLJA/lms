        userController.setDml(user, OperasiCRUD.INSERT);
        adminController.setDml(admin, OperasiCRUD.INSERT);
        
        JOptionPane.showMessageDialog(this, "Data berhasil ditambah!");
        
        AdminMenu_ViewAdminMouseClicked(null);
